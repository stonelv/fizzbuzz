package com.stonelv.fizzbuzz;

public class LengthConversion {
    public static int footToInch(int foot) {
        return foot * 12;
    }

    public static int yardToFoot(int yard) {
        return yard * 3;
    }

    public static int yardToInch(int yard) {
        return yard * 3 * 12;
    }
}
